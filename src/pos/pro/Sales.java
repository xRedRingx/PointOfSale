public void stckload() {
    DefaultTableModel tb = (DefaultTableModel) jTable1.getModel();
    int rc = tb.getRowCount();
    for (int i = 0; i < rc; i++) {
        String bcode = tb.getValueAt(i, 2).toString();
        String qty = tb.getValueAt(i, 3).toString();

        try {
            Connection con = db.mycon();
            PreparedStatement stmt = con.prepareStatement("SELECT qty FROM products WHERE bar_code = ?");
            stmt.setString(1, bcode);
            ResultSet rs = stmt.executeQuery();
            if (rs.next()) {
                Double stockquantity;
                stockquantity = Double.valueOf(rs.getString("qty"));
                Double sell_qty = Double.valueOf(qty);
                Double new_qty = stockquantity - sell_qty;
                String newquantity = String.valueOf(new_qty);

                PreparedStatement updateStmt = con.prepareStatement("UPDATE products SET qty = ? WHERE bar_code = ?");
                updateStmt.setString(1, newquantity);
                updateStmt.setString(2, bcode);
                updateStmt.executeUpdate();
            }

        } catch (NumberFormatException | SQLException e) {
            System.out.println(e);
        }
    }
}
    
public void addDataToTable() {
    String stockQtyText = stk_qty.getText();
    String sellQtyText = pro_qty.getText();

    if (stockQtyText.isEmpty() || sellQtyText.isEmpty()) {
        JOptionPane.showMessageDialog(paid_amount, "من فضلك أدخل رقما صحيحا");
        return;
    }

    double stockQty = Double.parseDouble(stockQtyText);
    double sellQty = Double.parseDouble(sellQtyText);

    if (sellQty <= stockQty) {
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        Vector<Object> rowData = new Vector<>();
        rowData.add(inid.getText());
        //rowData.add(combo_product.getSelectedItem().toString());
        rowData.add(select_pro_name);
        rowData.add(bar_c.getText());
        rowData.add(pro_qty.getText());
        rowData.add(unit_price.getText());
        rowData.add(tot_price.getText());

        tableModel.addRow(rowData);
        calculateCartTotal();
    } else {
        JOptionPane.showMessageDialog(paid_amount, "المخزون فيه " + stockQty);
    }
}

