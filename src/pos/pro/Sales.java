private void combo_productActionPerformed(java.awt.event.ActionEvent evt) {
    String name = combo_product.getSelectedItem().toString();
    
    try {
        Statement s = db.mycon().createStatement();
        ResultSet rs = s.executeQuery("SELECT bar_code,sell_price,qty,product_name FROM products WHERE product_name ='" + name + "'");
        if (rs.next()) {
            unit_price.setText(rs.getString("sell_price"));
            bar_c.setText(rs.getString("bar_code"));
            stk_qty.setText(rs.getString("qty"));
            select_pro_name = rs.getString("product_name");
        }
        
    } catch (SQLException e) {
        System.out.println(e);
    }
    
    pro_qty.setText("");
    pro_qty.requestFocus();
}

