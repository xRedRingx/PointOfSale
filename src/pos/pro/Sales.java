public void calculateProduct() {
    double quantity = Double.parseDouble(pro_qty.getText());
    double price = Double.parseDouble(unit_price.getText());
    double total = quantity * price;
    tot_price.setText(String.valueOf(total));
}
    
    
public void calculateCartTotal() {
    int numOfRows = jTable1.getRowCount();
    double totalPrice = 0;
    double totalQuantity = 0;

    for (int i = 0; i < numOfRows; i++) {
        double value = Double.parseDouble(jTable1.getValueAt(i, 5).toString());
        double quantity = Double.parseDouble(jTable1.getValueAt(i, 3).toString());
        totalPrice += value;
        totalQuantity += quantity;
    }

    total_price.setText(Double.toString(totalPrice));
    total_qty.setText(Double.toString(totalQuantity));
}
    
    
